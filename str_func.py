def capital_letters(value):
    """
    Фуекция принимает на вход строку и возвращает ее со всеми заглавными буквами
    """
    return value.upper()

def big_register(value):
    """
    Функция, которая делает заглавными первые буквы каждого слова в строке, поступившей на вход функции 1
    """
    return value.title()

